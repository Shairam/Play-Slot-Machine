export
class Symbol {
    private img: String;
    private val: number;

    
    
    
    public  setImage(image:String) {
        this.img = image;

    }

   
    public  getImage():String {
        return this.img;
    }

   
    public  setValue(value:number) {
       this.val = value; 

    }
    
    public  getValue():Number {
        return this.val; 

     }
    
    


}
